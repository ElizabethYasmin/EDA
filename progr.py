def bubbleSort(arr):
	start_time = time()

	n = len(arr)

	for i in range(n-1):
		for j in range(0, n-i-1):
			if arr[j] > arr[j + 1] :
				arr[j], arr[j + 1] = arr[j + 1], arr[j]

	elapsed_time = time()-start_time
	return elapsed_time

def heapify(arr, n, i):
	largest = i
	l = 2 * i + 1  
	r = 2 * i + 2   

	if l < n and arr[i] < arr[l]:
		largest = l

	if r < n and arr[largest] < arr[r]:
		largest = r
	if largest != i:
		arr[i],arr[largest] = arr[largest],arr[i] 
		heapify(arr, n, largest)

def heapSort(arr):
	start_time = time()

	n = len(arr)
	for i in range(n // 2 - 1, -1, -1):
		heapify(arr, n, i)

	for i in range(n-1, 0, -1):
		arr[i], arr[0] = arr[0], arr[i]  
		heapify(arr, i, 0)

	elapsed_time = time()-start_time
	return elapsed_time

