def insertion_sort(arr):
	start_time = time()
	

	x = len(arr)

	

	for i in range(1, x):
		key = arr[i]
		j = i-1
		while(j>= 0 and arr[j] > key):
			arr[j+1] = arr[j]
			j-=1
		arr[j+1] = key



	elapsed_time = time() - start_time
	#print("Elapsed time: %.10f seconds." % elapsed_time)
	return elapsed_time

